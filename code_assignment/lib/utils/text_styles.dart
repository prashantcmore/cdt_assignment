import 'package:flutter/material.dart';

TextStyle XlargeStyle(Color color) {
  return TextStyle(fontSize: 20, fontWeight: FontWeight.bold, color: color);
}

TextStyle largeStyle(Color color) {
  return TextStyle(fontSize: 18, fontWeight: FontWeight.w600, color: color);
}

TextStyle largeBoldStyle(Color color) {
  return TextStyle(fontSize: 18, fontWeight: FontWeight.w800, color: color);
}

TextStyle mediumStyle(Color color) {
  return TextStyle(fontSize: 16, fontWeight: FontWeight.w600, color: color);
}

TextStyle regularStyle(Color color) {
  return TextStyle(fontSize: 16, fontWeight: FontWeight.w600, color: color);
}

TextStyle lightStyle(Color color) {
  return TextStyle(fontSize: 16, fontWeight: FontWeight.w400, color: color);
}
